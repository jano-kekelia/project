True && True     // შედეგი: True, რადგან ორივე პირობა მართალია
True && False    // შედეგი: False, რადგან ერთ-ერთი პირობა ცრუა
False && True    //შედეგი: False, რადგან ერთ-ერთი პირობა ცრუა
False && False   // შედეგი: False, რადგან ორივე პირობა ცრუა


true || true     // true – ორივე პირობა ჭეშმარიტია
true || false    // true – ერთ-ერთი პირობა მაინც ჭეშმარიტია
false || true    // true – ერთ-ერთი პირობა მაინც ჭეშმარიტია
false || false   // false – ორივე პირობა მცდარია
